func findDisappearedNumbers(nums []int) []int {
	// åˆ©ç”¨æ•°ç»„è¿™ç§æ•°æ®ç»“æ„å»ºç«‹ n ä¸ªæ¡¶ï¼ŒæŠŠæ‰€æœ‰é‡å¤å‡ºç°çš„ä½ç½®è¿›è¡Œæ ‡è®°ï¼Œç„¶åå†éå†ä¸€éæ•°ç»„å³å¯æ‰¾åˆ°æ²¡æœ‰å‡ºç°è¿‡çš„æ•°å­—
	// è¿›ä¸€æ­¥åœ°ï¼Œæˆ‘ä»¬å¯ä»¥ç›´æ¥å¯¹åŸæ•°ç»„è¿›è¡Œæ ‡è®°ï¼šæŠŠé‡å¤å‡ºç°çš„æ•°å­—åœ¨åŸæ•°ç»„å‡ºç°çš„ä½ç½®è®¾ä¸ºè´Ÿæ•°ï¼Œæœ€åä»ç„¶ä¸ºæ­£æ•°çš„ä½ç½®å³ä¸ºæ²¡æœ‰å‡ºç°è¿‡çš„æ•°
	ans := make([]int, 0)
	for _, num := range nums {
		pos := int(math.Abs(float64(num)))-1
		if nums[pos] > 0 {
			nums[pos] = -nums[pos]
		}
	}
	for i, num := range nums {
		if num > 0 {
			ans = append(ans, i+1)
		}
	}
	return ans
}
