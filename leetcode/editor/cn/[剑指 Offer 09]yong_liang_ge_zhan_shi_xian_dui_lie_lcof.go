type CQueue struct {
	in, out stack
}


func ConstructorOffer08() CQueue {
	return CQueue{
		in: make(stack, 0),
		out: make(stack, 0),
	}
}


func (this *CQueue) AppendTail(value int)  {
	this.in.push(value)
}


func (this *CQueue) DeleteHead() int {
	if len(this.out) == 0 {
		n := len(this.in)
		for i := 0; i < n; i++ {
			x := this.in.top()
			this.out.push(x)
			this.in.pop()
		}
	}
	if len(this.out) == 0 {
		return -1
	}
	v := this.out.top()
	this.out.pop()
	return v
}

