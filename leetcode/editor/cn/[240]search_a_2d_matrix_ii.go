func searchMatrix(matrix [][]int, target int) bool {
	// è¿™é“é¢˜æœ‰ä¸€ä¸ªç®€å•çš„æŠ€å·§ï¼šæˆ‘ä»¬å¯ä»¥ä»å³ä¸Šè§’å¼€å§‹æŸ¥æ‰¾ï¼Œè‹¥å½“å‰å€¼å¤§äºå¾…æœç´¢å€¼ï¼Œæˆ‘ä»¬å‘å·¦
	// ç§»åŠ¨ä¸€ä½ï¼›è‹¥å½“å‰å€¼å°äºå¾…æœç´¢å€¼ï¼Œæˆ‘ä»¬å‘ä¸‹ç§»åŠ¨ä¸€ä½
	// å¦‚æœæœ€ç»ˆç§»åŠ¨åˆ°å·¦ä¸‹è§’æ—¶ä»ä¸ç­‰äºå¾…æœç´¢å€¼ï¼Œåˆ™è¯´æ˜å¾…æœç´¢å€¼ä¸å­˜åœ¨äºçŸ©é˜µä¸­
	m, n := len(matrix), len(matrix[0])
	if m < 1 {
		return false
	}
	i, j := 0, n-1
	for i < m && j >= 0 {
		temp := matrix[i][j]
		if temp == target {
			return true
		} else if temp > target {
			j--
		} else {
			i++
		}
	}
	return false
}
