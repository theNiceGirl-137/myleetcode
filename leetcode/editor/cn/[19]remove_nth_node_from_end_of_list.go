/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func removeNthFromEnd(head *ListNode, n int) *ListNode {
	var pre *ListNode
	newHead := &ListNode{Next: head}
	cur := newHead
	if head == nil {
		return head
	}
	count := 1
	for head != nil {
		if count >= n {
			pre = cur
			cur = cur.Next
		}
		count++
		head = head.Next
	}
	pre.Next = pre.Next.Next
	return newHead.Next
}
